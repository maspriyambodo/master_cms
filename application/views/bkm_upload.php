            function Tingkat(val) {
                if (val == 2) {
                    $('#kabupaten').removeAttr('disabled');
                    $('#kecamatan').attr('disabled', '');
                    $('#kelurahan').attr('disabled', '');
                } else if (val == 3) {
                    $('#kabupaten').removeAttr('disabled');
                    $('#kecamatan').removeAttr('disabled');
                    $('#kelurahan').attr('disabled', '');
                } else if (val == 4) {
                    $('#kabupaten').removeAttr('disabled');
                    $('#kecamatan').removeAttr('disabled');
                    $('#kelurahan').removeAttr('disabled');
                } else {
                    $('#kabupaten').attr('disabled', '');
                    $('#kecamatan').attr('disabled', '');
                    $('#kelurahan').attr('disabled', '');
                }
            }
            function Provinsi(id) {
                $('#kabupaten').children('option').remove();
                $('#kecamatan').children('option').remove();
                $('#kelurahan').children('option').remove();
                $.ajax({
                    url: "<?php echo base_url('bkm-getkab?id_provinsi='); ?>" + id,
                    type: 'get',
                    dataType: 'json',
                    cache: false,
                    success: function (data) {
                        var i;
                        for (i = 0; i < data.length; i++) {
                            var sel = document.getElementById("kabupaten");
                            var opt = document.createElement("option");
                            opt.value = data[i].id_kabupaten;
                            opt.text = data[i].kabupaten;
                            sel.add(opt, sel.options[i]);
                        }
                    },
                    error: function () {
                        Swal.fire(
                                'Error',
                                'error ketika mengambil data kabupaten',
                                'error'
                                );
                    }
                });
            }
            function Kecamatan(id) {
                $('#kelurahan').children('option').remove();
                $.ajax({
                    url: "<?php echo base_url('bkm-getkec?id_kec='); ?>" + id,
                    type: 'get',
                    dataType: 'json',
                    cache: false,
                    success: function (data) {
                        var i;
                        for (i = 0; i < data.length; i++) {
                            var sel = document.getElementById("kecamatan");
                            var opt = document.createElement("option");
                            opt.value = data[i].id_kecamatan;
                            opt.text = data[i].kecamatan;
                            sel.add(opt, sel.options[i]);
                        }
                    },
                    error: function () {
                        Swal.fire(
                                'Error',
                                'error ketika mengambil data kecamatan',
                                'error'
                                );
                    }
                });
            }
            function Kelurahan(id) {
                $('#kelurahan').children('option').remove();
